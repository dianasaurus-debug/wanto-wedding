     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (request()->query('cari')) {
            $allusers = User::where('nama_depan', 'like', '%' . request()->query('cari') . '%')
                ->where('is_admin', 0)
                ->orWhere('nama_belakang', 'like', '%' . request()->query('cari') . '%')
                ->with('booking.product')
                ->latest()
                ->paginate(7);
        } else {
            $allusers = User::where('is_admin', 0)
                ->with('booking.product')
                ->latest()
                ->paginate(7);
        }
        return Inertia::render('Customer/Index', ['all_users' => $allusers, 'status_bookings' => config('constants.booking_status')]);
    }

